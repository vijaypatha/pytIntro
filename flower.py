import turtle

def vi():
    v = turtle.Turtle()
    
    v.right(120)
    v.forward(300)
    
    v.right(120)
    v.forward(300)
vi()

def ja():
    j = turtle.Turtle()
    j.forward(120)
    j.right(90)
    j.forward(250)
    j.right(90)
    j.forward(100)
    j.right(90)
    j.forward(80)
    
ja()
